export function flagUrl(slug: string, size: 'w20' | 'w40' | 'w80' | 'w160' = 'w80'): string | null {
  const code = slugToIso2[slug];
  if (!code) return null;
  return `https://flagcdn.com/${size}/${code}.png`;
}

export function flagUrlSrcset(slug: string): string | null {
  const code = slugToIso2[slug];
  if (!code) return null;
  return `https://flagcdn.com/w40/${code}.png 1x, https://flagcdn.com/w80/${code}.png 2x`;
}
